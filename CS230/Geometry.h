/********************************************************************************************************************************/
/*											Geometry (Base Class) - Declarations												*/
/********************************************************************************************************************************/


class Geometry
{
	//=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-Virtual Functions=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
		
	public:

	virtual int		area() = 0;													//compute the area of the shape
	virtual int		perimeter() = 0;											//compute the perimeter of the shape
	virtual void	translate(double dx,double dy) = 0;							//move the shape
	virtual void	draw(ostream &os) = 0;										//print a description to os (see below)



	//=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-Non-Virtual Functions=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
	void			setColor(Color newColor);									//set the color of the shape
	Color			getColor();													//get the color of the shape	
	
};	

